from pymonntorch import Behavior
import torch


class InpSyn(Behavior):
	def initialize(self, ng):
		ng.I = torch.tensor(ng.I_inp)

	def forward(self, ng):
		ng.I = torch.tensor(ng.I_inp)
		for syn in ng.afferent_synapses["All"]:
			ng.I += syn.I 
